		if((enable>>1)){
			TIM1->CCR1=THIPWM.TIMCOMPA;
			TIM1->CCR2=THIPWM.TIMCOMPB;
			TIM1->CCR3=THIPWM.TIMCOMPC;
		}else if(!(enable>>1)){
			TIM1->CCR1=0;
			TIM1->CCR2=0;
			TIM1->CCR3=0;
		}
		HAL_GPIO_WritePin(test_GPIO_Port, test_Pin, 1);
